    // ========================
    //   CARGAR TABLA
    // ========================
    public void cargarSolicitudes(List<SolicitudInspeccion> lista) {
        DefaultTableModel modelo = getModelo();
        modelo.setRowCount(0);

        for (SolicitudInspeccion s : lista) {
            modelo.addRow(new Object[]{
                s.getIdSolicitud(),
                s.getNumLote(),
                s.getFechaAsignacion(),
                s.getEstado()
            });
        }
    }

